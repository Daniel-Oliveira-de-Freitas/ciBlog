<h2>O Que É Um Blog? Uma Introdução ao Blogging</h2>
<img src="assets\images\O-Que-E-Um-Blog.png">
<br>
<p>Um blog é um site ou parte de um site que contém conteúdo frequentemente atualizado sobre um ou múltiplos tópicos. O termo é uma contração para “web log”, que por sua vez significa registrar informações num site.

    O conteúdo num blog vem na forma de artigos em páginas individuais chamados postagens de blog (blog posts). O artigo que você está lendo é um exemplo.

    Na página inicial do blog, você vai encontrar uma coleção dessas postagens apresentadas em ordem cronológica reversa – da mais recente para a mais antiga. A página de Tutoriais da Hostinger é uma boa ilustração para isso.</p>
